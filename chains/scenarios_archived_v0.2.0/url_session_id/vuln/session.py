"""Session management module -- VULNERABLE variant.

Session tokens are accepted from the URL query string (?sid=...) in addition
to cookies.  An attacker can craft a URL with a known session ID, send it to a
victim, and hijack the session once the victim authenticates -- a classic
session fixation via URL parameter (CWE-384).

The token value from the URL is promoted to an authenticated session just like
a cookie-based token, giving the attacker full access.

Chain: attacker crafts URL with known sid -> victim clicks link and logs in ->
       attacker reuses sid as authenticated session
Individual findings: session token in URL (medium)
Chain finding: session fixation via URL parameter leads to account takeover (critical)
"""
import secrets
import time
import functools
from flask import request, jsonify

_sessions = {}


def create_session(token=None):
    """Create or reuse a session keyed by token.

    VULNERABLE: if a caller-supplied token is passed in, it is stored as-is
    without verifying that it was generated by this server.  This is the
    entry point for URL-based fixation.
    """
    if token is None:
        token = secrets.token_hex(32)
    _sessions.setdefault(token, {
        "user_id": None,
        "role": "anonymous",
        "authenticated": False,
        "created_at": time.time(),
    })
    return token


def authenticate_session(token, user_id, role):
    """Mark an existing session as authenticated in place (no regeneration)."""
    if token not in _sessions:
        token = secrets.token_hex(32)
        _sessions[token] = {"created_at": time.time()}
    _sessions[token].update({
        "user_id": user_id,
        "role": role,
        "authenticated": True,
    })
    return token


def validate_session(token):
    """Return session data for a valid authenticated token, else None."""
    if not token:
        return None
    session = _sessions.get(token)
    if session is None or not session.get("authenticated"):
        return None
    return session


def require_auth(f):
    """Decorator that accepts session token from cookie or URL parameter."""
    @functools.wraps(f)
    def decorated(*args, **kwargs):
        token = request.cookies.get("sid") or request.args.get("sid", "")
        session = validate_session(token)
        if session is None:
            return jsonify({"error": "Authentication required"}), 401
        request.session = session
        return f(*args, **kwargs)
    return decorated
